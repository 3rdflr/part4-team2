// 페이지별 리뷰를 가져오는 함수
export const getMockReviews = (
  _activityId: number, // activityId는 현재 사용하지 않지만 API 호환성을 위해 유지
  page: number = 1,
  size: number = 10,
): ReviewsResponse => {
  const startIndex = (page - 1) * size;
  const endIndex = startIndex + size;
  const paginatedReviews = mockReviews.slice(startIndex, endIndex);

  const totalRating = mockReviews.reduce((sum, review) => sum + review.rating, 0);
  const averageRating = Math.round((totalRating / mockReviews.length) * 10) / 10;

  return {
    averageRating,
    totalCount: mockReviews.length,
    reviews: paginatedReviews,
  };
};
