const generateRandomReview = (id: number): Review => {
  const rating =
    Math.random() < 0.6
      ? Math.random() < 0.7
        ? 5
        : 4
      : Math.random() < 0.7
        ? 3
        : Math.random() < 0.8
          ? 2
          : 1;
  const templates = reviewTemplates[rating as keyof typeof reviewTemplates];
  const content = templates[Math.floor(Math.random() * templates.length)];
  const nickname = nicknames[Math.floor(Math.random() * nicknames.length)];

  // 날짜 생성 (최근 6개월 내)
  const now = new Date();
  const sixMonthsAgo = new Date();
  sixMonthsAgo.setMonth(now.getMonth() - 6);
  const randomTime =
    sixMonthsAgo.getTime() + Math.random() * (now.getTime() - sixMonthsAgo.getTime());
  const createdAt = new Date(randomTime).toISOString();

  return generateMockReview(id, id + 100, `${nickname}${id}`, rating, content, createdAt);
};

